public class Constantes {
	
	public static void main(String[] args) {
		
		final float PI = 3.1416f;
		
		float radio = 16f;
		
		float perimetro = radio*PI*2;
		
		System.out.println("El perímetro de una circunferencia de radio " + radio + " es " + perimetro);
		
	}
}


